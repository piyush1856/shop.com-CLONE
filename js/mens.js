function displayProduct(index){
    document.querySelector("#prodect").innerHTML="";
    index.map(function(elem){
        let  div = document.createElement("div");
        let img = document.createElement("img");
        img.src = elem.image;
        
        let name = document.createElement("span");
        name.innerHTML = elem.name;

        let sold = document.createElement("p");
        sold.innerText= `Solod by ${elem.sold}`;

        let pricediv = document.createElement("div");
        pricediv.setAttribute("id", "pricediv");

        let  price = document.createElement("span");
        price.innerText=`$${elem.price.toFixed(2)}`;

        let shipping = document.createElement("p");
        shipping.innerText="Free shipping";
        shipping.style.color="teal";

        let button = document.createElement("button");
        button.innerText="Choose Options";
        button.addEventListener('click',function(){
            isproduct(elem)
        });

        div.append(img,name,sold,price,shipping,button);

        document.querySelector("#prodect").append(div);
    });
}


